-- MAX, MIN e AVG

SELECT MAX(JANEIRO) AS MAIOR_JAN
FROM VENDEDORES;

SELECT MIN(FEVEREIRO) AS MENOR_FEV
FROM VENDEDORES;

SELECT AVG(MARCO) AS MEDIA_MAR
FROM VENDEDORES;

-- TRUNCATE para valores FLOAT

SELECT TRUNCATE(AVG(MARCO), 2) AS MEDIA_MAR
FROM VENDEDORES;

-- SUM PARA O TOTAL

SELECT SUM(JANEIRO) AS TOTAL_JAN
FROM VENDEDORES;


-- GROUP BY para exibir as informações agregadas segundo um certo padrão

SELECT SEXO, MIN(FEVEREIRO) AS MENOR_FEV
FROM VENDEDORES
GROUP BY SEXO;

